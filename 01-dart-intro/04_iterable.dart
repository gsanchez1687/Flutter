//List, Iterables y Sets
void main(){
  final numbers = [1,2,3,4,5,5,6,7,8,9,10];
  
  print('Lista de original: $numbers');

  print('Cantidad: ${numbers.length} ');
  
  print('First: ${ numbers.first } ');
  
  print('Ultimo: ${ numbers.last } ');
  
  print('Reverse: ${ numbers.reversed } ');
  
}